package comm02.op;

public class No01_ArithmaticDemo {
/*
 * 산술연산자
 * + ,- , *, / , % 
 * 단항연산자
 * + ,- , ++, --, !
 * 등호연산자 
 * ==, !=, > , >= , <, <= , 
 * 조건연산자
 * && , || , ?: 
 * 객체비교연산자
 * instanceof 
 */
	public static void main(String[] args) {
		int i =5;
		i=i+1 ; 
		System.out.println("i=i+i의 결과값 : " +i);
		
		int j =5;
		j++;
		System.out.println("j++의 결과값 : " +j);
		
		int k =5; 
		++k ; 
		System.out.println("++k의 결과값 : " +k);
			

	}
}
